function flattenLocalizationPatch(node, prefix = '') {
  return Object.entries(node).reduce((acc, [key, value]) => {
    const nextKey = prefix ? `${prefix}.${key}` : key
    if (value && typeof value === 'object' && !Array.isArray(value)) {
      Object.assign(acc, flattenLocalizationPatch(value, nextKey))
    } else {
      acc[nextKey] = value
    }
    return acc
  }, {})
}

